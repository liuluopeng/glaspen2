use std::sync::Mutex;
use windows::Win32::Foundation::*;
use windows::Win32::UI::WindowsAndMessaging::*;
use windows::Win32::System::LibraryLoader::GetModuleHandleW;

use super::overlay;
use super::render;

static HOOK_HANDLE: Mutex<isize> = Mutex::new(0);
static HOOK_HWND: Mutex<isize> = Mutex::new(0);
static mut PEN_DRAWING: bool = false;
static mut STROKE_COLOR: (f64, f64, f64) = (1.0, 0.0, 0.0);

/// Install the global low-level mouse hook on the current thread.
pub fn install_hook(hwnd: HWND) {
    let hmodule = unsafe { GetModuleHandleW(None).unwrap() };
    let instance = HINSTANCE(hmodule.0);
    let hook = unsafe {
        SetWindowsHookExW(WH_MOUSE_LL, Some(mouse_hook_proc), instance, 0).unwrap()
    };
    let mut h = HOOK_HANDLE.lock().unwrap();
    *h = hook.0 as isize;
    set_hook_hwnd(hwnd);
}

/// Uninstall the mouse hook.
pub fn uninstall_hook() {
    let h = HOOK_HANDLE.lock().unwrap();
    if *h != 0 {
        unsafe {
            let hook = HHOOK(*h as *mut _);
            UnhookWindowsHookEx(hook).ok();
        }
    }
    unsafe {
        if PEN_DRAWING {
            ShowCursor(TRUE);
            PEN_DRAWING = false;
        }
    }
}

fn set_hook_hwnd(hwnd: HWND) {
    let mut h = HOOK_HWND.lock().unwrap();
    *h = hwnd.0 as isize;
}

fn get_hook_hwnd() -> Option<HWND> {
    let h = HOOK_HWND.lock().unwrap();
    if *h == 0 {
        None
    } else {
        Some(HWND(*h as *mut _))
    }
}

/// Check if an event was generated by a pen/tablet.
fn is_pen_event(extra_info: usize) -> bool {
    if extra_info == 0 {
        return false;
    }
    let val = extra_info as u32;
    // High-bit patterns common with tablet pen drivers (Huion, Wacom)
    if val > 0x100 {
        return true;
    }
    false
}

